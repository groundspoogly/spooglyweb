/* Accidentals Guide

Official Note Set:
- Fifth-generated scales use the XFCGDAEB cycle, BP and orgone have their own systems, and others use notes counting backward from Z
- AB, BC, etc. names for notes exactly between naturals
- "+" and "-" represent alterations by a patent val syntonic comma
- All remaining sharps and flats, including E#, B#, Cb, Fb
- Accidentals may be represented with an alteration by a syntonic comma to mark their function in relation to the circle of fifths
- Up and down versions of the remaining notes (unless double accidentals or half accidentals provide a better name)
- Doing something else for notes that would require double ups or double downs (like double sharps/flats or ABb, A#-B, etc. names)

dark notes prefer to be sharped
bright notes prefer to be flatted

11edo smitonic, generator = 8\11
 x   x x   x
A B C D E F G A

12edo diatonic, generator = 7\12
 x   x x   x x
A B C D E F G A

13edo oneirotonic, generator = 5\13
 x   x x   x x 
A C B D F E G X A

16edo superdiatonic, generator = 9\16
 x   x x x x   x x
A B X C D E Y F G A

*/

